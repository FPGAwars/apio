@click.command(
    name="get-system",
    cls=ApioCommand,
    short_help="Retrieve apio and system information.",
    help=APIO_API_GET_SYSTEM_HELP,
)
# @click.pass_context
@timestamp_option
@output_option
@options.force_option_gen(short_help="Overwrite output file.")
def _get_system_cli(
    # Options
    timestamp: str,
    output: str,
    force: bool,
):
    """Implements the 'apio apio get-system' command."""

